// 응급 의도 감지 — 안전장치.
//
// 본 서비스는 "이 병원이 무엇에 주력하나"(선택진료)를 보여주는 도구이지 응급 트리아지가
// 아니다. 절단·의식소실·호흡곤란 같은 응급을 의미검색으로 외래 클리닉에 매칭하면 위험하다.
// → 응급 신호가 감지되면 병원 추천 대신 "119/응급실" 안내를 최상단에 띄운다(의료법상으로도
//   특정 병원을 응급에 '추천'하지 않는 게 안전).
//
// 보수적으로(거짓양성 최소화) 명백한 응급어만 매칭. '가슴 성형'·'골절 재활' 같은 선택진료
// 표현과 충돌하지 않도록 응급 맥락이 분명한 패턴만 둔다.

const EMERGENCY_PATTERNS: RegExp[] = [
  /절단|잘렸|잘랐|잘려(서|나|요)?|베여서 피/, // 절단·심한 열상
  /의식\s*(이)?\s*(없|잃|안)|기절|쓰러[졌짐]|정신을\s*잃/, // 의식 소실
  /호흡\s*곤란|숨\s*(을|이)?\s*(못|안|막)|숨쉬기\s*힘/, // 호흡
  /대량\s*출혈|피가\s*(안|멈추지|계속)|출혈이\s*심|피가\s*많이/, // 출혈
  /경련|발작|쥐가\s*나서 의식|심정지|심장\s*마비|심장이\s*멈/, // 경련·심정지
  /음독|중독.*(쓰러|의식|구토)|약을\s*(많이|과다)\s*먹/, // 중독
  /(심한|크게|많이)\s*화상|화상을\s*(크게|심하게)/, // 중증 화상
  /119|응급실|응급\s*상황|당장\s*병원|죽을\s*것\s*같/, // 직접 응급 표현
];

/** 자연어 쿼리가 응급 상황을 시사하면 true. */
export function isEmergencyQuery(query: string | null | undefined): boolean {
  if (!query) return false;
  const q = query.trim();
  if (!q) return false;
  return EMERGENCY_PATTERNS.some((re) => re.test(q));
}
