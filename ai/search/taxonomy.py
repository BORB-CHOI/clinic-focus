def tag_hospital(text: str, standard_specialty: str) -> list[str]:
    """병원 콘텐츠(크롤 본문 등)에서 해당 진료과의 시술 태그를 매칭한다.

    진료과가 taxonomy 에 없으면 빈 리스트(태그 미지원 종별: 종합병원·요양병원 등).
    매칭은 키워드 부분문자열 포함 여부. 등장 태그를 순서 보존해 반환.

    Args:
        text: 병원 콘텐츠(자체 사이트 + 외부 시그널 합본 텍스트).
        standard_specialty: HIRA 기준 표준 진료과목(22 후보 중 하나).

    Returns:
        매칭된 시술 태그 라벨 리스트 (고정 vocabulary). 필터칩·primary_focus 입력.
    """
    tags = SPECIALTY_TAGS.get(standard_specialty)
    if not tags or not text:
        return []
    matched: list[str] = []
    for label, keywords in tags:
        if any(kw in text for kw in keywords):
            matched.append(label)
    return matched
